public class Productos {
    private String id;

    private int stock;

    private String tipo;

    private double precio;

    private String nombre;

    public Productos(String id, int stock, String tipo, double precio, String nombre) {
        this.id = id;
        this.stock = stock;
        this.tipo = tipo;
        this.precio = precio;
        this.nombre = nombre;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
}
